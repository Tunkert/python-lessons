age = 46  # int
full_name = "Timothy Unkert"  # string
pi = 3.14  # float
numbers = [1, 56, 43, 22, 99]  # array of numbers, called a list in python
is_programmer = True
my_dict = {
    "f_name": "Tim",
    "l_name": "Unkert",
    "age": 46
}
my_tup = (4, "Tim", 11, 17, 8, True)
my_set = {"Tim", 12, True, False, "hello", 3.66}

print(type(age))  # <class 'int'>
print(type(full_name))  # <class 'str'>
print(type(pi))  # <class 'float'>
print(type(numbers))  # <class 'list'>
print(type(is_programmer))  # <class 'bool'>
print(type(my_dict))  # <class 'dict'>
print(type(my_tup))  # <class 'tuple'>
print(type(my_set))  # <class 'set'>

