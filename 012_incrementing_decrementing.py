num_1 = 0

print(num_1)

num_1 += 5  # increment num_1 by 5

print(num_1)

num_1 -= 4  # decrement num_1 by 4

print(num_1)

# multiplication and division

num_1 *= 12  # multiply current value of num_1 by 12

print(num_1)

num_1 //= 5  # integer divide num_1 by 5

print(num_1)

num_1 /= 1.2   # divide by 1.2 - we get 1.6666666

print(num_1)
