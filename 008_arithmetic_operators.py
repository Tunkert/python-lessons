num_1 = 3
num_2 = 7

print(num_1 + num_2)  # 10
print(num_2 - num_1)  # 4
print(num_1 - num_2)  # -4
print(num_1 * num_2)  # 21
print(num_1 / num_2)  # 0.42857
print(num_2 / num_1)  # 2.33333
print(2 / 1)  # 2.0 - always a float with division
print(num_2 % num_1)  # 1 - the remainder
print(2 ** 3)  # 8, because 2 to the 3rd power is 8
print(num_2 // num_1)  # 2 - integer division